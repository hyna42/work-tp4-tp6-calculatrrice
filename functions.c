#include "functions.h"

// Fonction addition : calculates and returns the sum of two numbers
float add(float a, float b)
{
    return a + b;
}

// Fonction subtraction : calculates and returns the subtract of two numbers
float sub(float a, float b)
{
    return a - b;
}

// Fonction multiplication : calculates and returns the product of two numbers
float mul(float a, float b)
{
    return a * b;
}

// Fonction division : calculates and returns the quotient of two numbers
float diviz(float a, float b)
{
    return a / b;
}
